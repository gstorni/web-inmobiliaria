// Helper functions
export function getTagById(id: number) {
  return TOKKO_PROPERTY_TAGS.find((tag) => tag.id === id)
}

export function getTagsByType(type: number) {
  return TOKKO_PROPERTY_TAGS.filter((tag) => tag.type === type)
}

export function getIndustrialTags() {
  return INDUSTRIAL_PROPERTY_TAGS
}

export function getTagsByCategory(category: keyof typeof TAG_CATEGORIES) {
  const categoryData = TAG_CATEGORIES[category]
  return TOKKO_PROPERTY_TAGS.filter((tag) => categoryData.tags.includes(tag.id))
}

export function isIndustrialTag(tagId: number): boolean {
  return INDUSTRIAL_PROPERTY_TAGS.some((tag) => tag.id === tagId)
}

// Format tags for display
export function formatTagsForDisplay(tagIds: number[]): string[] {
  return tagIds
    .map((id) => {
      const tag = getTagById(id)
      return tag ? tag.nameEs || tag.name : null
    })
    .filter(Boolean) as string[]
}
